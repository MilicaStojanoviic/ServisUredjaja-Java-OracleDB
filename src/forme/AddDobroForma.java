    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            if (this.operation.equals("INSERT")) {
                this.insert();
            } else if (this.operation.equals("UPDATE")) {
                this.update();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            adjustFields(d);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void adjustFields(VrstaDobra d) {
        txtNaziv.setText(d.getNaziv());
        txtCena.setText(String.valueOf(d.getAktuelnaCena()));
        cmbJM.setSelectedItem(d.getJm());
        btnAdd.setText("Izmeni");
    }

    


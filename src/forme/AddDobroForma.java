    private void fillCmbJM() {
        try {
            cmbJM.removeAllItems();
            List<JedinicaMere> jm = Controller.getInstance().getJMList();
            for (JedinicaMere j : jm) {
                cmbJM.addItem(j);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja dobra!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void insert() throws Exception {
         if(txtNaziv.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Morate popuniti sva polja!");
            return;
        }
        String naziv=txtNaziv.getText();
        JedinicaMere jm=(JedinicaMere) cmbJM.getSelectedItem();
        
        VrstaDobra d=new VrstaDobra();
        d.setNaziv(naziv);
        d.setJm(jm);
        d.setAktuelnaCena(0);
        
        Controller.getInstance().insert(d);
        JOptionPane.showMessageDialog(this, "Uspesno ste unesi dobro!", "Uspesno", JOptionPane.INFORMATION_MESSAGE);
        parent.refreshTable();
        this.setVisible(false);
        dispose();
    }

    private void update() throws Exception {
        if(txtNaziv.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Morate popuniti sva polja!");
            return;
        }
        String naziv=txtNaziv.getText();
        JedinicaMere jm=(JedinicaMere) cmbJM.getSelectedItem();
        double aktCena = Double.parseDouble(txtCena.getText());
        
        VrstaDobra d=new VrstaDobra();
        if(aktCena!=this.d.getAktuelnaCena()){
            d.setCenaIzmena(true);
        }
        d.setSifraDobra(this.d.getSifraDobra());
        d.setNaziv(naziv);
        d.setJm(jm);
        d.setAktuelnaCena(0);
        
        Controller.getInstance().update(d);
        JOptionPane.showMessageDialog(this, "Uspesno ste unesi dobro!", "Uspesno", JOptionPane.INFORMATION_MESSAGE);
        parent.refreshTable();
        this.setVisible(false);
        dispose();
    }

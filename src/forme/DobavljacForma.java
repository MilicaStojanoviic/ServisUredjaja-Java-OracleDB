    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(tblDobavljac.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati dobavljaca koda zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Dobavljac d = ((TableModelDobavljac) tblDobavljac.getModel()).getSelectedObject(tblDobavljac.getSelectedRow());
                AddDobavljacForma form=new AddDobavljacForma(this, true, d, "UPDATE");
                form.setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(tblDobavljac.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati dobavljaca koda zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Dobavljac d = ((TableModelDobavljac) tblDobavljac.getModel()).getSelectedObject(tblDobavljac.getSelectedRow());
                Controller.getInstance().delete(d);
                refreshTable();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    

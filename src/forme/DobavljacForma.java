    private void fillTable() {
        try {
            List<Dobavljac> list = Controller.getInstance().getDobavljacList();
            TableModelDobavljac modelD = new TableModelDobavljac(list);
            tblDobavljac.setModel(modelD);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska prilikom ucitavanja!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    void refreshTable() {
        List<Dobavljac> shownD = loadObjects();
        TableModelDobavljac modelD = (TableModelDobavljac) tblDobavljac.getModel();
        modelD.setList(shownD);
        modelD.fireTableDataChanged();
    }

    private List<Dobavljac> loadObjects() {
        try {
            return Controller.getInstance().getDobavljacList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }
